   ORG 00H
   JMP INIT
   ORG 0500H
LED MACRO DATA, MA
   MOV A, DATA
   ORL A, #MA
   MOV P1, A
   MOV R0, #0FH
   DJNZ R0, $
   ENDM
INIT:
   MOV R1, #00H
   MOV R2, #00H
   MOV R3, #00H
   MOV R4, #00H
MAIN:
   ACALL SHOW
   INC R2
   CJNE R2, #0AH, MAIN
   MOV R2, #00H
   INC R3
   CJNE R3, #02H, MAIN
STEADY:
   LED R1, 70H
   LED R2, 0B0H
   LED R3, 0D0H
   LED R4, 0E0H
   SJMP STEADY
SHOW:
   MOV R5, #0FFH
SHOW1:
   MOV R6, #010H
SHOW2:
   LED R1, 70H
   LED R2, 0B0H
   LED R3, 0D0H
   LED R4, 0E0H
   DJNZ R6, SHOW2
   DJNZ R5, SHOW1
   RET
   END
