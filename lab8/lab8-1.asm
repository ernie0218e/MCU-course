    ORG 00H
    JMP INIT
    ORG 23H
    JMP INTERRUPT
    ORG 50H
INIT:
    MOV SCON, #01010000B
    ANL PCON, #01111111B
    MOV IE, #10010000B
    MOV TMOD, #00100000B
    MOV TH1, #0F3H
    JMP $
INTERRUPT:
    JNB RI, END_INTER
    CLR RI
    MOV A, SBUF
    MOV SBUF, A
END_INTER:
    CLR TI
    RETI
    END
